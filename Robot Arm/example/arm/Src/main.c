void example(void){

	//turn right
	 if(mode[0] == 1 && mode[2] == 1){
		 MOTOR_PWM[0]--;
		 if (MOTOR_PWM[0]<380)
			 mode[0]=2;
		 HAL_Delay(10);
	 }
	 //grab an object
	 else if(mode[0] == 2 && mode[2] == 1){
		MOTOR_PWM[2]--;
		if (MOTOR_PWM[2]<215){
			mode[2] = 2;
		}
		HAL_Delay(10);
	 }
	 //turn left
	 else if(mode[0] == 2 && mode[2] == 2){
		 MOTOR_PWM[0]++;
		 if(MOTOR_PWM[0]>620)
			 mode[0]=0;
		 HAL_Delay(10);
	 }
	 //drop the object
	 else if (mode[0] == 0 && mode[2] == 2){
		MOTOR_PWM[2]++;
		if (MOTOR_PWM[2]>430){
			mode[2] = 0;
		}
		HAL_Delay(10);
	}

}
